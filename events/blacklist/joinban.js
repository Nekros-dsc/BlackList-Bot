const bl = require('../../me-modals/blacklist/blacklisted')
module.exports = {
	name: 'guildMemberAdd',
async execute(client, member) {
    let DATA = await bl.findOne({UserID: member.id})
    if (DATA.UserID) {
        member.ban({ reason: DATA.Reason }).catch(e);
    }
	}
}
